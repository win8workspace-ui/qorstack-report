import { copyFileSync, existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(__dirname, '..', '..')
const src = resolve(repoRoot, 'selfhost')
const publicDest = resolve(__dirname, '..', 'public', 'selfhost')
const generatedDest = resolve(__dirname, '..', 'src', 'generated')

const files = ['docker-compose.yml', '.env.example']

if (!existsSync(publicDest)) mkdirSync(publicDest, { recursive: true })
if (!existsSync(generatedDest)) mkdirSync(generatedDest, { recursive: true })

const contents = {}

for (const f of files) {
  const from = resolve(src, f)
  const to = resolve(publicDest, f)
  if (!existsSync(from)) {
    console.warn(`[sync-selfhost-public] missing source (skipped): ${from}`)
    contents[f] = ''
    continue
  }
  copyFileSync(from, to)
  contents[f] = readFileSync(from, 'utf8')
  console.log(`[sync-selfhost-public] static: public/selfhost/${f}`)
}

const banner = `// Auto-generated by frontend/scripts/sync-selfhost-public.mjs — do not edit by hand.\n// Source: /selfhost/*\n`

const tsFile =
  banner +
  `\nexport const dockerComposeYaml = ${JSON.stringify(contents['docker-compose.yml'])}\n` +
  `\nexport const envExample = ${JSON.stringify(contents['.env.example'])}\n`

const tsOut = resolve(generatedDest, 'selfhost-files.ts')
writeFileSync(tsOut, tsFile)
console.log(`[sync-selfhost-public] embed:  src/generated/selfhost-files.ts`)
